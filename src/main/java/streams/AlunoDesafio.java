package streams;

public class AlunoDesafio {
    final String nome;
    final double nota;

    final Integer falta;

    final boolean bomComportamento;



    public AlunoDesafio(String nome, double nota, Integer falta, boolean bomComportamento) {
        this.nome = nome;
        this.nota = nota;
        this.falta=falta;
        this.bomComportamento=bomComportamento;
    }
}
